package com.spring.recopay.domain;

public class MypageDTO {
	private int seatUid;
	private String thName;
	private String thLocation;
	private String prfName;
	private String prfTime;
	private String seatNum;
	
	
	public int getSeatUid() {
		return seatUid;
	}
	public void setSeatUid(int seatUid) {
		this.seatUid = seatUid;
	}
	public String getThName() {
		return thName;
	}
	public void setThName(String thName) {
		this.thName = thName;
	}
	public String getThLocation() {
		return thLocation;
	}
	public void setThLocation(String thLocation) {
		this.thLocation = thLocation;
	}
	public String getPrfName() {
		return prfName;
	}
	public void setPrfName(String prfName) {
		this.prfName = prfName;
	}
	public String getPrfTime() {
		return prfTime;
	}
	public void setPrfTime(String prfTime) {
		this.prfTime = prfTime;
	}
	public String getSeatNum() {
		return seatNum;
	}
	public void setSeatNum(String seatNum) {
		this.seatNum = seatNum;
	}
	
	
}
